# 4. Дан список студентов и их курсов:
students = [
    ("Анна", "Python"),
    ("Борис", "Java"),
    ("Виктор", "Python"),
    ("Галина", "C++"),
    ("Дмитрий", "Python")
]
x = {} # Создайте словарь, в котором ключами будут названия курсов, а значениями — списки студентов, которые изучают этот курс.
for name, grade in students:
    if grade not in x:
        x[grade] = [name]
    else:
        x[grade].append(name)
print(x)

