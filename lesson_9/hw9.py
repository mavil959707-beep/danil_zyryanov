x = {"Ваня": 2, "Егор": 3, "Ян": 4, "Витя": 5} # 5. Создайте словарь, содержащий оценки студентов:
min_score = min(x.values()) # Используя метод pop(), удалите студента с самой низкой оценкой и выведите обновленный словарь.
for name, score in x.items():
    if score == min_score:
        student_to_remove = name
        break
x.pop(student_to_remove)
print(x)

